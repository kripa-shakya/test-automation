package org.example.prapti.chapter2;

public class EvenOdd {
    public static void main(String[] args) {
        int a=2;
        System.out.println("a :" + a );
        if (a % 2 == 0) {
            System.out.println("a is even");
        } else {
            System.out.println("a is odd");
        }
            

    }
}
