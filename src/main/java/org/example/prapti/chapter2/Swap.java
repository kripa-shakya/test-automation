package org.example.prapti.chapter2;

public class Swap {
    public static void main(String[] args) {
        int a = 2, b = 3;
        System.out.println("a :" + a + " b :" + b);
        int test;
        test=a;
        a=b;
        b=test;
        System.out.println("a :" + a + " b :" + b);
    }
}
